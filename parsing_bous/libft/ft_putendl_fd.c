#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	len;
	int	i;

	i = 0;
	len = ft_strlen(s);
	while (len > 0)
	{
		write(fd, s + i, 1);
		len--;
		i++;
	}
	write(fd, "\n", 1);
}
