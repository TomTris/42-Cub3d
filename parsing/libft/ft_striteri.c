#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	len;

	len = (int) ft_strlen(s);
	while (len > 0)
	{
		(*f)(len - 1, s + len - 1);
		len--;
	}
}
