#include "libft.h"

// is alphabet / num or underscore
int	ft_isalnum_(int a)
{
	if (((a > 47) && (a < 58))
		|| ((a > 64) && (a < 91))
		|| ((a > 96) && (a < 123))
		|| a == '_')
		return (1);
	else
		return (0);
}
